import { test, expect } from 'vitest';
import { assertFormat, assertFormatInvalid } from './utils.ts';

test('conditional parts', () => {
  expect.assertions(39);
  assertFormat('[>=100]"A"0;[<=-100]"B"0;"C"0', 6.3, 'C6');
  assertFormat('[>=100]"A"0;[<=-100]"B"0;"C"0', -6.3, '-C6');
  assertFormat('[>=100]"A"0;[<=0]"B"0;"C"0', 6.3, 'C6');
  assertFormat('[>=100]"A"0;[<=0]"B"0;"C"0', -6.3, '-B6');
  assertFormat('[>=0]"A"0;[<=-100]"B"0;"C"0', 6.3, 'A6');
  assertFormat('[>=0]"A"0;[<=-100]"B"0;"C"0', -6.3, '-C6');
  assertFormat('[>=0]"A"0;[<=0]"B"0;"C"0', 6.3, 'A6');
  assertFormat('[>=0]"A"0;[<=0]"B"0;"C"0', -6.3, '-B6');
  assertFormat('[>=0]"A"0;[<=-100]"B"0', 6.3, 'A6');
  assertFormat('[>=100]"A"0;[<=0]"B"0', -6.3, '-B6');
  assertFormat('[<=-100]"B"0;"C"0', 6.3, 'C6');
  assertFormat('[<=-100]"B"0;"C"0', -6.3, 'C6');
  assertFormat('[<=0]"B"0', 6.3, '6.3');
  assertFormat('[<=0]"B"0', -6.3, '-B6');
  assertFormat('[=6.3]"B"0', 6.3, 'B6');
  assertFormat('[=6.3]"B"0', -6.3, '-6.3');
  assertFormat('[<>6]"B"0', 6.3, 'B6');
  assertFormat('[<>6]"B"0', -6.3, '-B6');
  assertFormat('[=6.3]"B"0', 6.3, 'B6');
  assertFormat('[=6.3]"B"0', -6.3, '-6.3');
  assertFormat('[>6]"A"0;[<-6]"B"0', 6.3, 'A6');
  assertFormat('[>6]"A"0;[<-6]"B"0', -6.3, 'B6');
  assertFormat('[>=100]"A"0;[<=-100]"B"0;"C"0;"D"@', 6.3, 'C6');
  assertFormat('[>=100]"A"0;[<=-100]"B"0;"C"0;"D"@', -6.3, '-C6');
  assertFormat('[>=100]"A"-0;[<=-100]"B"-0;"C"-0', 6.3, 'C-6');
  assertFormat('[>=100]"A"-0;[<=-100]"B"-0;"C"-0', -6.3, '-C-6');
  assertFormat('[>=100]"A"-#,##0;[<=0]"B"-#,##0;"C"-#,##0', 6.3, 'C-6');
  assertFormat('[>=100]"A"-#,##0;[<=0]"B"-#,##0;"C"-#,##0', -6.3, '-B-6');
  assertFormat('[>=0]"A"-0;[<=-100]"B"-0;"C"-0', 6.3, 'A-6');
  assertFormat('[>=0]"A"-0;[<=-100]"B"-0;"C"-0', -6.3, '-C-6');
  assertFormat('[>=0]"A"-0;[<=0]"B"-0;"C"-0', 6.3, 'A-6');
  assertFormat('[>=0]"A"-0;[<=0]"B"-0;"C"-0', -6.3, '-B-6');

  assertFormat('[>=100]"A"0;[<=-100]"B"0', 6.3, '######');
  assertFormat('[>=100]"A"0;[<=-100]"B"0', -6.3, '######');
  assertFormat('[>=0]"A"0;[<=-100]"B"0', -6.3, '######');
  assertFormat('[>=100]"A"0;[<=0]"B"0', 6.3, '######');
  assertFormatInvalid('[>=100]"A"0;[<=-100]"B"0;"C"0;"D"0');
  assertFormatInvalid('[>=100]"A"0;[<=-100]"B"0;"C"0;"D"0');
  assertFormatInvalid('[<-25]0;[>25]0;0;"foo".#');
});

